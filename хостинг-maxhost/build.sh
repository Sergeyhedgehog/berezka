#!/bin/bash
# ──────────────────────────────────────────────────────────────────────────────
# build.sh — копирует актуальные файлы сайта в папку "для-загрузки"
# Запускать из директории хостинг-maxhost/: bash build.sh
# ──────────────────────────────────────────────────────────────────────────────

SRC="$(dirname "$0")/.."
DST="$(dirname "$0")/для-загрузки"

echo "→ Копируем файлы в для-загрузки/ ..."

cp "$SRC/index.html"    "$DST/"
cp "$SRC/style.css"     "$DST/"
cp "$SRC/app.js"        "$DST/"
cp "$SRC/sw.js"         "$DST/"
cp "$SRC/manifest.json" "$DST/"
cp "$SRC/icon-192.png"  "$DST/"
cp "$SRC/icon-512.png"  "$DST/"

echo "✓ Готово. Файлы в папке для-загрузки/:"
ls -lh "$DST"
